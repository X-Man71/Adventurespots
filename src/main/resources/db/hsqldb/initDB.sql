);

CREATE TABLE rating(
    ID INTEGER IDENTITY PRIMARY KEY,
    NAME VARCHAR(255)
);

CREATE TABLE activity(
    ID INTEGER IDENTITY PRIMARY KEY,
    NAME VARCHAR(255),
    RATING_ID INTEGER,
    SPOT_SPOTID INTEGER
);

CREATE TABLE advertisement(
    ID INTEGER IDENTITY PRIMARY KEY,
    NAME VARCHAR(255),
    SPOT_SPOTID INTEGER,
